const ELEMENT_MAP: Record<string, string> = {};
ELEMENTS.forEach(el => {
  ELEMENT_MAP[el.symbol.toLowerCase()] = el.symbol;
  ELEMENT_MAP[el.name.toLowerCase()] = el.symbol;
});

/**
 * Resolves a mix of symbols and names into a distinct set of valid element symbols.
 */
export function resolveInputTokens(input: string): string[] {
  const parts = input.split(/[\s,]+/).map(p => p.trim().toLowerCase()).filter(Boolean);
  const symbols = new Set<string>();
  
  for (const p of parts) {
    if (ELEMENT_MAP[p]) {
      symbols.add(ELEMENT_MAP[p]);
    }
  }
  return Array.from(symbols);
}

/**
 * Robustly parses a chemical formula into a distinct list of elements, resolving hydrates/parentheses.
 * Example: "CuSO4·5H2O" -> ["Cu", "S", "O", "H"]
 */
export function extractElementsFromFormula(formula: string): string[] {
  if (!formula) return [];
  // Strip common non-element formula characters (dots, water hydrates, explicit prefixes)
  // E.g. "·5H2O" -> we just match the H and O normally.
  const regex = /[A-Z][a-z]?/g;
  const matches = formula.match(regex) || [];
  
  // Verify that the scraped elements are real elements (to ignore things like "Ph" if it was loosely written)
  return Array.from(new Set(matches)).filter(m => ELEMENT_MAP[m.toLowerCase()]);
}

/**
 * Strict check: returns true if the compound's formula exists ONLY of the provided symbols.
 */
export function isStrictSubset(compoundFormula: string, allowedSymbols: string[]): boolean {
  if (!compoundFormula || compoundFormula === '?') return false;
  const elementsInCompound = extractElementsFromFormula(compoundFormula);
  if (elementsInCompound.length === 0) return false;
  
  const allowedSet = new Set(allowedSymbols);
  for (const el of elementsInCompound) {
    if (!allowedSet.has(el)) return false;
  }
  return true;
}
