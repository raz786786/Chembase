def seed_database():
    """Populate the database with initial chemistry data."""
    Base.metadata.create_all(bind=engine)
    db = SessionLocal()

    # Check if already seeded
    if db.query(Substance).count() > 0:
        db.close()
        return

    substance_map = {}  # name -> Substance object

    # Seed elements
    for el in ELEMENTS:
        s = Substance(
            name=el["name"], formula=el["formula"], type="element",
            molar_mass=el["molar_mass"], description=el["description"],
            state_at_room_temp=el["state_at_room_temp"], color=el["color"],
            density=el["density"], melting_point=el["melting_point"],
            boiling_point=el["boiling_point"],
            electronegativity=el.get("electronegativity"),
            atomic_number=el["atomic_number"], symbol=el["symbol"],
            group_number=el["group_number"], period=el["period"],
            category=el["category"],
            electron_configuration=el["electron_configuration"],
            block=el.get("block"), year_discovered=el.get("year_discovered"),
            is_radioactive=el.get("is_radioactive", False),
            atomic_radius=el.get("atomic_radius")
        )
        db.add(s)
        db.flush()
        substance_map[el["name"]] = s
        substance_map[el["symbol"]] = s # map by symbol as well for reaction resolution
    
    # Add Oxygen as O2 for reactions to work properly since element is just O
    o2 = Substance(
        name="Oxygen Gas", formula="O2", type="compound",
        molar_mass=31.998, state_at_room_temp="gas"
    )
    db.add(o2)
    db.flush()
    substance_map["Oxygen"] = o2
    
    h2 = Substance(
        name="Hydrogen Gas", formula="H2", type="compound",
        molar_mass=2.016, state_at_room_temp="gas"
    )
    db.add(h2)
    db.flush()
    substance_map["Hydrogen"] = h2

    n2 = Substance(
        name="Nitrogen Gas", formula="N2", type="compound",
        molar_mass=28.014, state_at_room_temp="gas"
    )
    db.add(n2)
    db.flush()
    substance_map["Nitrogen"] = n2

    # Seed compounds
    for comp in COMPOUNDS:
        s = Substance(
            name=comp["name"], formula=comp["formula"], type="compound",
            cas_number=comp.get("cas_number"), molar_mass=comp.get("molar_mass"),
            description=comp.get("description"),
            state_at_room_temp=comp.get("state_at_room_temp"),
            color=comp.get("color"), density=comp.get("density"),
            melting_point=comp.get("melting_point"),
            boiling_point=comp.get("boiling_point"),
            block=comp.get("block")
        )
        db.add(s)
        db.flush()
        substance_map[comp["name"]] = s

        if comp.get("hazard"):
            h = HazardData(substance_id=s.id, **comp["hazard"])
            db.add(h)

    db.flush()

    # Seed reactions
    for rxn in REACTIONS:
        r = Reaction(
            name=rxn["name"], equation=rxn["equation"],
            reaction_type=rxn.get("reaction_type"), conditions=rxn.get("conditions"),
            enthalpy_change=rxn.get("enthalpy_change"),
            is_reversible=rxn.get("is_reversible", False),
            description=rxn.get("description"),
            industrial_value_tier=rxn.get("industrial_value_tier"),
            verification_status=rxn.get("verification_status", "predicted"),
            safety_notes=rxn.get("safety_notes")
        )
        for rn in rxn.get("reactant_names", []):
            if rn in substance_map:
                r.reactants.append(substance_map[rn])
        for pn in rxn.get("product_names", []):
            if pn in substance_map:
                r.products.append(substance_map[pn])
        db.add(r)

    db.commit()
    db.close()
    print("Database seeded successfully!")
